# Function to add two numbers
def add(a, b):
    c = a + b
    print("Addition:", c)

# Function to subtract two numbers
def sub(a, b):
    c = a - b
    print("Subtraction:", c)

# Function to multiply two numbers
def mul(a, b):
    c = a * b
    print("Multiplication:", c)

# Function to divide two numbers
def div(a, b):
    c = a / b
    print("Division:", c)
